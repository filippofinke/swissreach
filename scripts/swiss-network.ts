// unique 7-digit ids starting with 85.

export type Mode =
  | 'RAIL'
  | 'BUS'
  | 'TRAM'
  | 'SUBWAY'
  | 'TROLLEYBUS'
  | 'FUNICULAR'
  | 'AERIAL_LIFT'
  | 'FERRY';

export type Station = {
  id: number; // real Swiss DiDok/UIC station number, e.g. 8507000 for Bern. Unique.
  name: string; // official station name, e.g. "Bern", "Zürich HB", "Genève"
  lat: number; // WGS84 latitude  (Switzerland ~ 45.8..47.8)
  lon: number; // WGS84 longitude (Switzerland ~ 5.9..10.5)
};

export type Line = {
  name: string; // e.g. "IC1", "IR15", "S1", "RE", or a bus line label
  mode: Mode;
  stops: number[]; // ordered list of Station ids the line serves (>= 2)
  headwayMin: number; // service interval in minutes (e.g. 30 for half-hourly, 60 hourly)
  firstDep: string; // first departure from the first stop, "HH:MM" (e.g. "05:00")
  lastDep: string; // last departure from the first stop, "HH:MM" (e.g. "23:30")
  speedKmh?: number; // optional average speed override; sensible defaults per mode if omitted
  dwellMin?: number; // optional dwell time at intermediate stops, default 1
};

